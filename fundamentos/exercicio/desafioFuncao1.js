// Criar uma função 2 parametros -> somar
// Criar uma função 2 parametros -> subtração

function somaNumeros(num1, num2) {
  console.log(Number.parseInt(num1) + Number.parseInt(num2));
}

somaNumeros(1, 2);
somaNumeros(2, 2);

function subtrairNumeros(num1, num2) {
  console.log(Number.parseInt(num1) - Number.parseInt(num2));
}

subtrairNumeros('2', '1');
subtrairNumeros('4', '4');
