let x = 10;

/**
 *   += : operação valor da variável = variável + alguma coisa
 *   -= : operação valor da variável = variável - alguma coisa
 *   *= : operação valor da variável = variável * alguma coisa
 *   /= : operação valor da variável = variável / alguma coisa
 *   %= : operação valor da variável = variável % alguma coisa
 */

x += 6;
x -= 1;
x *= 6;
x /= 5;
x %= 2;

