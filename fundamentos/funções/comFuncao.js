/**
 * ==============================FUNÇÃO=======================================
 * Funções são blocos de construção fundamentais em JavaScript.
 * Uma função é um procedimento de JavaScript - um conjunto de instruções
 * que executa uma tarefa ou calcula um valor. Para usar uma função, você deve
 * defini-la em algum lugar no escopo do qual você quiser chamá-la.
 */

const dia1 = 3;
const mes1 = 7;
const ano1 = 2022;

function ixibirData1() {
  console.log(`${dia1}/${mes1}/${ano1}`);
}

const dia2 = 23;
const ano2 = 2023;
const mes2 = 12;

function ixibirData2() {
  console.log(`${dia2}-${mes2}-${ano2}`);
}

ixibirData1();
ixibirData2();
